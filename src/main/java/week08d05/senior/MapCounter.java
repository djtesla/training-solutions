package week08d05.senior;

public class MapCounter {

    private char innerChar;
    private int counter;

    public MapCounter(char innerChar, int counter) {
        this.innerChar = innerChar;
        this.counter = counter;
    }

    public char getInnerChar() {
        return innerChar;
    }

    public int getCounter() {
        return counter;
    }
}
